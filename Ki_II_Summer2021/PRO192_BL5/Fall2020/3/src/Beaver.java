public class Beaver {
    private String river;
    private int weight;

    public Beaver() {
    }

    public Beaver(String river, int weight) {
        this.river = river;
        this.weight = weight;
    }

    public String getRiver() {
        return river;
    }

    public void setRiver(String river) {
        this.river = river;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
    
    
    
    
}
